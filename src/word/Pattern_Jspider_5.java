public class Pattern_Jspider_5 {
	public static void main(String[] args) {
		String st="JSIDERS";
		for(int i=0;i<st.length();i++)
		{
			for(int j=0;j<st.length();j++)
			{
				System.out.print(st.charAt(j)+" ");
			}
			System.out.println();
		}
		}
}
