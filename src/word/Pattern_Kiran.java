package word;
/*
K K K K K 
I I I I I 
R R R R R 
A A A A A 
N N N N N 

*/
public class Pattern_Kiran {
	
	public static void main(String[] args) {
		String st="KIRAN";
		for(int i=0;i<st.length();i++)
		{
			for(int j=0;j<st.length();j++)
			{
				System.out.print(st.charAt(i)+" ");
			}
			System.out.println();
		}
		}
}
